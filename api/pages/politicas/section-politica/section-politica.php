    <h2 class="section-politica__title">Política de reembolso</h2>
    <p class="section-politica__p">
      Tenemos una política de cambio de 5 días, lo que significa que tiene 5 días después de recibir su artículo para solicitar un cambio de producto.
      Los únicos motivos para realizar un cambio de producto son los siguientes: <br />
      1. Que el producto recibido no sea el mismo que se adquirió en nuestra tienda online.<br />
      2. Que el producto llegue dañado o roto.<br /> <br />
      Para ser elegible para un cambio, su artículo debe estar en las mismas condiciones en que lo recibió, sin usar, con etiquetas y en su empaque original.
      El correo electrónico a enviar con la solicitud de cambio debe incluir una explicación del problema, así como fotos y videos como evidencia adicional.
      Para iniciar un cambio, puede contactarnos en soporte ana.sumacc@gmail.com
      Si su cambio es aceptado, le enviaremos una etiqueta de envío de devolución, así como instrucciones sobre cómo y dónde enviar su paquete.
    </p>
    
    <h2 class="section-politica__title">Daños y problemas</h2>
    <p class="section-politica__p">
      Inspeccione su pedido al recibirlo y comuníquese con nosotros de inmediato si el artículo está defectuoso, dañado o si recibe el artículo incorrecto, para que podamos evaluar el problema y corregirlo.</p>

    <h2 class="section-politica__title">Excepciones / artículos no retornables</h2>
